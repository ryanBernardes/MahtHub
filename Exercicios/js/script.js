var num1 = parseInt(prompt("Digite o número 1"));
var num2 = parseInt(prompt("Digite o número 2"));
soma = num1+num2
subtracao = num1-num2
multiplicaco = num1*num2
divisao = num1/num2

document.write("<p>A soma dos números é: </p>",soma);
document.write("<p>A subtração dos números é: </p>",subtracao);
document.write("<p>A multiplicação dos números é: </p>",multiplicaco);
document.write("<p>A divisâo dos números é: </p>",divisao);